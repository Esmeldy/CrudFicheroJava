    private void bt_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_buscarActionPerformed
        Productos p = new Productos();
        String a = p.buscar(Cod_producto.getText());
        txt_area.setText(a);
    }//GEN-LAST:event_bt_buscarActionPerformed

    private void bt_mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_mostrarActionPerformed
        Productos p = new Productos();
        String Mostrar="";
        try {
            Mostrar = p.mostrar();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FormularioGestion.class.getName()).log(Level.SEVERE, null, ex);
        }
        txt_area.setText(Mostrar);
    }//GEN-LAST:event_bt_mostrarActionPerformed

