    private void bt_InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_InsertarActionPerformed
        Productos p = new Productos(Integer.parseInt(Cod_producto.getText()), nom_producto.getText(),
                Integer.parseInt(cant_producto.getText()), Desc_producto.getText());
        String resultado = p.altas();
        txt_area.setText(resultado);
       
    }//GEN-LAST:event_bt_InsertarActionPerformed

